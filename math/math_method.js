//common math properties are used 
/*Math.abs() - return absolute value of a number.
Math.ceil() - it rounds up and return smaller integer
Math.exp() - return exponential  raised to the power of a number.
Math.trunc() - it return integer part of a number by removing fractional part 
Math.sqrt() - returns square root of a given number.
Math.round() - return value of a number rounded to the nearest integer.
Math.pow() - return base raised to the power.
Math.min() - return smallest of the given number.
Math.max() - return largest of the given number.
Math.floor() - rounds down and return the largest integer less than or equal to given number.
*/


